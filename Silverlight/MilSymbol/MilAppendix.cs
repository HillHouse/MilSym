        private static readonly List<HashSet<string>> NoTemplates = new List<HashSet<string>>
        {
            NoTemplateA, NoTemplateB, NoTemplateC, NoTemplateD, NoTemplateE, NoTemplateG
        };

        /// <summary>
        /// Returns whether the template (stencil) is present in the list of missing stencils.
        /// </summary>
        public static bool NoTemplate(string template, string appendix=null)
        {
            if (appendix != null && appendix.Length == 1)
            {
                switch (appendix)
                {
                    case "A": return NoTemplateA.Contains(template);
                    case "B": return NoTemplateB.Contains(template);
                    case "C": return NoTemplateC.Contains(template);
                    case "D": return NoTemplateD.Contains(template);
                    case "E": return NoTemplateE.Contains(template);
                    case "G": return NoTemplateG.Contains(template);
                }
                return false;
            }
            else
            {
                foreach (var noTemplate in NoTemplates)
                {
                    if (noTemplate.Contains(template))
                    {
                        return true;
                    }
                }
            }
            return false;
        }

        /// <summary>
        /// Returns the standard's symbol codes for the given appendix.
        /// </summary>
        /// <param name="match">Regular expression for the symbol codes to return</param>
        /// <returns>An enumerable list of strings representing the appendix symbol codes or an empty list if no codes exist</returns>
        public static IEnumerable<string> Keys(string match)
        {
            var il = new List<string>();
            try
            {
                var regex = new Regex(match);

                //// Used to be:
                //// 
                //// foreach (var d in DictionaryList)
                //// {
                ////     IEnumerable<string> keys = d.Keys;
                ////     foreach (var s in keys)
                ////     {
                ////         if (regex.Match(s).Success)
                ////         {
                ////             il.Add(s);
                ////         }
                ////     }
                //// }
                foreach (ICollection<string> keys in DictionaryList.Select(d => d.Keys))
                {
                    il.AddRange(keys.Where(s => regex.Match(s).Success));
                }
            }
            catch (Exception ex)
